// ── i18n ENGINE ──
let currentLang = localStorage.getItem('omnex-lang') || 'uk';

function t(key) {
  const entry = translations[key];
  if (!entry) return key;
  return entry[currentLang] || entry['uk'] || key;
}

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('omnex-lang', lang);
  document.documentElement.lang = lang === 'uk' ? 'uk' : lang === 'cs' ? 'cs' : 'en';
  applyTranslations();
  // Dispatch event for other components to react
  window.dispatchEvent(new CustomEvent('languageChanged', { detail: { lang } }));
}

function getLanguage() {
  return currentLang;
}

function applyTranslations() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    const text = t(key);
    if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
      el.placeholder = text;
    } else if (el.tagName === 'OPTION') {
      el.textContent = text;
    } else {
      el.textContent = text;
    }
  });
  // Handle HTML content
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.dataset.i18nHtml;
    el.innerHTML = t(key);
  });
}

// Language Switcher
function initLanguageSwitcher() {
  const switcher = document.getElementById('langSwitcher');
  if (!switcher) return;

  const buttons = switcher.querySelectorAll('button');
  buttons.forEach(btn => {
    if (btn.dataset.lang === currentLang) btn.classList.add('active');
    btn.addEventListener('click', () => {
      buttons.forEach(b => b.classList.remove('active'));
      btn.classList.add('active');
      setLanguage(btn.dataset.lang);
    });
  });
}

export { t, setLanguage, getLanguage, applyTranslations, initLanguageSwitcher, translations };
